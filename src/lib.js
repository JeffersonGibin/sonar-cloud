
const sum = (a, b) => {
    return a + b;
};

const subtract = (a, b) => {
    return a - b;
};

const divide = (a, b) => {
    return a / b;
};

const multiply = (a, b) => {
    return a * b;
};

const power = (a, b) => {
    return a ** b;
};

const modulo = (a, b) => {
    return a % b;
};


export { sum, subtract, divide, multiply, power, modulo };